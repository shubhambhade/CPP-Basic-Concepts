#include <iostream>
using namespace std;

int main()
{
    cout<<"Variable and Comments\n";
    int a = 10;
    cout<<"A : "<<a<<endl;
    float b = 12.33;
    cout<<"B : "<<b<<endl;
    double c = 12.3456;
    cout<<"C : "<<c<<endl;
    bool d = true;
    cout<<"D : "<<d<<endl;
    char e = 'A';
    cout<<"E : "<<e;
    return 0; 
}

